// Weather-to-Mood Rules for Hindi Speakers (Weather App or News Channel)

pub fn get_mood_tip_hindi(weather: &str, temp_c: Option<f32>) -> &'static str {
    match (weather, temp_c) {
        ("rainy", Some(temp)) if temp < 15.0 => "इनडोर साँस लेने का अभ्यास करें और गरम पेय लें।",
        ("rainy", _) => "घर के अंदर शांत संगीत सुनें।",
        ("sunny", _) => "मूड बढ़ाने के लिए 10 मिनट टहलें।",
        ("cloudy", _) => "अपनी पसंदीदा किताब पढ़ें या दोस्त को कॉल करें।",
        ("cold", _) => "5 मिनट हल्की स्ट्रेचिंग करें।",
        ("hot", Some(temp)) if temp > 30.0 => "पानी पिएँ और छाँव में आराम करें।",
        ("hot", _) => "हल्के कपड़े पहनें और हाइड्रेट रहें।",
        ("stormy", _) => "गहरी साँस लें और सुरक्षित रूप से घर में रहें।",
        ("windy", _) => "इनडोर हॉबी या खेल खेलें।",
        ("foggy", _) => "स्पष्टता के लिए माइंडफुल मेडिटेशन करें।",
        ("humid", _) => "ठंडी बौछार लें और तरल पदार्थ पिएँ।",
        ("snowy", _) => "घर में हल्का योग करके गर्म रहें।",
        ("hail", _) => "घर के अंदर रहें और आरामदायक ध्वनियाँ सुनें।",
        ("thunderstorm", _) => "सुरक्षित रहें, पढ़ें या जर्नल लिखें।",
        ("drizzle", _) => "घर पर हल्की स्ट्रेचिंग करें।",
        ("clear", _) => "ताज़ी हवा लेने के लिए बाहर जाएँ।",
        ("overcast", _) => "घर के अंदर रचनात्मक गतिविधि आज़माएँ।",
        ("blizzard", _) => "कंबल ओढ़ें और पसंदीदा मूवी देखें।",
        ("sleet", _) => "गरम पेय लें और आराम करें।",
        _ => "खुद से जुड़ें और आरामदायक कुछ करें।"
    }
}